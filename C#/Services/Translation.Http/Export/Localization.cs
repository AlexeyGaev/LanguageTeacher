namespace Translation.Http {
    public static class Localization {
        static string isNull = "is Null";
        static string isEmpty = "is Empty";
        static string dot = ".";
        static string newLine = "\n";
        static string colon = ":";
        static string indent = " ";
        static string startBlock = "(";
        static string endBlock = ")";
        static string dash = "-";

        public static string IsNull { get { return isNull; } }
        public static string IsEmpty { get { return isEmpty; } }
        public static string Dot { get { return dot; } }
        public static string NewLine { get { return newLine; } }
        public static string Colon { get { return colon; } }
        public static string Indent { get { return indent; } }
        public static string StartBlock { get { return startBlock; } }
        public static string EndBlock { get { return endBlock; } }
        public static string Dash { get { return dash; } }
    }
}